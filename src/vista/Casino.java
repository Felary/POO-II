    private void btn_iniciar_ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_iniciar_ActionPerformed
        int tiempoMiliSegundos = 0;
        switch (cbox_dificultad.getSelectedIndex()) {
            case 0:
                tiempoMiliSegundos = 0;
                break;
            case 1:
                tiempoMiliSegundos = 100;
                break;
            case 2:
                tiempoMiliSegundos = 500;
                break;
            default:
                tiempoMiliSegundos = 0;
        }
        persona1 = new Persona1(tiempoMiliSegundos);
        persona2 = new Persona2(tiempoMiliSegundos);
        persona3 = new Persona3(tiempoMiliSegundos);

        respuesta1 = false;
        respuesta2 = false;
        respuesta3 = false;
        btn_iniciar_.setEnabled(false);
        persona1.start();
        persona2.start();
        persona3.start();
    }//GEN-LAST:event_btn_iniciar_ActionPerformed

